/*  • Condições simples
    Condicações simples são aquelas que contem apenas um if
    
    if (condicao) {
        True
    }

*/

/* • Condições compostas
    Condições compostas são formadas por if e else

    if (condicao) {
        True
    } else {
        False
    }

*/

/* • Condiçõies aninhadas
    São condições dentro de outra condições

    if (condicao) {
        bloco1
    } else {
        if (condicao) {
            bloco2
        } else {
            bloco3
        }
    }

            OU

    if (condicao) {
        true
    } else if {
        true
    } else {
        false
    }

*/

